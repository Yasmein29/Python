a = 3
b = 5.4
c = True
d = "Hello!"

print("Data type of a:", type(a))  # Output: <class 'int'>
print("Data type of b:", type(b))  # Output: <class 'float'>
print("Data type of c:", type(c))  # Output: <class 'bool'>
print("Data type of d:", type(d))  # Output: <class 'str'>
print("after type casting")
a = float(a)
b = int(b)
print("Data type of a:", type(a))  # Output: <class 'int'>
print("Data type of b:", type(b))  # Output: <class 'float'>